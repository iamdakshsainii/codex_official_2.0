    function HandwrittenNotesPage({ category }) {
      const notes = notesData[category] || [];
      const [selectedNote, setSelectedNote] = useState(null);

      const handleNoteClick = (note) => {
        setSelectedNote(note);
      };

      const handleCloseModal = () => {
        setSelectedNote(null);
      };

      return (
        <div className="container mx-auto py-12 px-4">
          <h1 className="text-4xl font-bold text-center mb-8">Handwritten Notes for {category}</h1>
          <p className="text-gray-400 text-center mb-8">
            Explore handwritten notes that offer a unique perspective on complex topics.
          </p>
          <ClickableNotesList notes={notes} onItemClick={category === 'frontend' || category === 'oops' || category === 'ai' || category === 'core-subjects' ? handleNoteClick : null} />
          {selectedNote && (
            <ResourceModal
              isOpen={selectedNote !== null}
              onClose={handleCloseModal}
              note={selectedNote}
              resources={selectedNote.resources}
            />
          )}
        </div>
      );
    }

